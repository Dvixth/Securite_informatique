def reconstruct_message_from_ciphertexts(ciphertexts):
    # Initialiser le message original
    original_message = ""

    # La longueur du message chiffré est de 26 caractères
    message_length = len(ciphertexts[0])

    # Pour chaque position de 1 à 26
    for i in range(1, message_length + 1):
        # Créer un ensemble de toutes les lettres possibles
        possible_letters = set("ABCDEFGHIJKLMNOPQRSTUVWXYZ")

        # Pour chaque message chiffré
        for ciphertext in ciphertexts:
            # Ajouter la lettre à l'ensemble
            possible_letters.discard(ciphertext[i - 1])

        # La lettre manquante est ajoutée au message original
        original_message += possible_letters.pop()

    return original_message

